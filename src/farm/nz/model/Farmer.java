package farm.nz.model;

public class Farmer {
	private String name;
	private int age;

	public Farmer() {
	}

	public Farmer(String name, int age) {
		this.name = name;
		this.age = age;
	}

	public int getAge() {
		return age;
	}

	public String getName() {
		return name;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public void setName(String name) {
		this.name = name;
	}

}
